// 2. FizzBuzz
// Напишите функцию, которая выводит числа от 1 до 100. Но для кратных трём
// выводите "Fizz" вместо числа, а для кратных пяти —
// "Buzz"
// . Для чисел, кратных как
// трём, так и пяти, выводите "FizzBuzz"

// Решение:
// 1. Вспомнить оператор деления с остатком, кажется %
// 2. Нужно сделать цикл от 1 до 100
// 2. Нужно проверить каждое число ли оно трём или пяти и выводить строки в зависимости от условия

function fizzBuzz() {
  for (let num = 1; num <= 100; num++) {
    if (num % 5 === 0 && num % 3 === 0) {
      console.log("FizzBuzz");
    } else if (num % 5 === 0) {
      console.log("Buzz");
    } else if (num % 3 === 0) {
      console.log("Fizz");
    } else {
      console.log(num);
    }
  }
}

fizzBuzz();

