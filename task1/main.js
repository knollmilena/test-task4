// 1. Проверка на палиндром
// Напишите функцию, которая проверяет , является ли строка палиндромом. Палиндром
// — это слово, фраза, число или другая последовательность символов, которая
// читается одинаково слева направо и справа налево (игнорируя пробелы, знаки
// препинания и регистр).

// Решение:
// 1. Нужно убрать все пробелы и знаки препинания
// 2. Нужно преобразовать строку в нижний регистр
// 3. Нужно сделать реверс строки
// 4. Нужно сравнить строку с ее обратной частью

function isPalindrome(str) {
  str = str.replace(/[^\w]/g, "");
  str = str.toLowerCase();
  const reversStr = str.split("").reverse().join("");

  return reversStr === str;
}

isPalindrome("А роза упала на лапу Азора");
isPalindrome("Привет");

